# string. Nos permite a nosotros representar texto.
titulo_curso= 'Curso profesional de Python'
print(titulo_curso)

mensaje = '''Te encuentras 
en el curso profesional de Python
en CódigoFacilito'''
print(mensaje)

# int. Nos permite trabajar con números entreros negativos o positivos.
numero_uno = 10
print(numero_uno)

numero_unon = 10 + 20
print(numero_unon)

division_entera = 10 // 3
print(division_entera)

division_decimal = 10 / 3
print(division_decimal)

# float. Cuando queremos trabajar con números reales, con punto decimal.
numero_dos = 3.14
print(numero_dos)

# bool. Solamente puede almacenar dos valores, ya sea verdadero o falso
valor = True
print(valor)

valor_dos = False
print(valor_dos)