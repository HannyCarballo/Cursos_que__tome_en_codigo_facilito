# Tenemos dos opciones para crear una lista

# 1) Se usa la función list, de esta forma se le indica a python que la variable será una lista
lista = list()

# 2) Se trabaja con corchetes, es exactamente lo mismo
#            0      1     2     3
lista = ['string', 10, 23.45, True]

print(lista)

# Lo recomendable es usar listas de un solo tipo de dato